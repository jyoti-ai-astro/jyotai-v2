// src/app/api/predictions/[id]/share/route.ts
import { NextRequest, NextResponse } from "next/server";
import { adminAuth, adminDb } from "@/lib/firebase-admin";

export const runtime = "nodejs";
export const dynamic = "force-dynamic";

/**
 * We intentionally return SVG (image/svg+xml) to avoid any native binary
 * dependencies (e.g., resvg). This eliminates the Webpack .node bundling error
 * on Vercel. If you ever need PNG/JPG again, we can switch to next/og (ImageResponse)
 * in a separate edge-only route that fetches via REST.
 */

function escapeText(s: string) {
  return (s || "").replace(/[<>&]/g, "");
}

function svgTemplate(title: string, body: string) {
  const safeTitle = escapeText(title).slice(0, 140);
  const safeBody = escapeText(body).slice(0, 1200);

  // 1200 × 630 social size (SVG is fine for app previews and downloads)
  return `<?xml version="1.0" encoding="UTF-8"?>
<svg width="1200" height="630" viewBox="0 0 1200 630" xmlns="http://www.w3.org/2000/svg">
  <defs>
    <linearGradient id="bg" x1="0" y1="0" x2="1" y2="1">
      <stop offset="0" stop-color="#0B0F14"/>
      <stop offset="1" stop-color="#1B2230"/>
    </linearGradient>
    <style>
      .h1 { font: 700 40px/1.2 ui-sans-serif, Inter, system-ui, -apple-system; fill: #FFC857; }
      .h2 { font: 600 28px/1.3 ui-sans-serif, Inter, system-ui, -apple-system; fill: #E6E6E6; }
      .body { font: 400 22px/1.45 ui-sans-serif, Inter, system-ui, -apple-system; fill: #CFD8E3; }
      .mono { font: 600 18px/1.2 ui-monospace, SFMono-Regular, Menlo, Monaco; fill: #7DD3FC; }
      .pill { fill: #18202C; stroke: #2A9DF4; stroke-width: 1; rx: 16; ry: 16; }
    </style>
  </defs>

  <rect width="1200" height="630" fill="url(#bg)"/>

  <!-- Header -->
  <text x="60" y="110" class="h1">JyotAI — Divine Reading</text>

  <!-- Title -->
  <text x="60" y="165" class="h2">${safeTitle}</text>

  <!-- Body -->
  <foreignObject x="60" y="200" width="1080" height="360">
    <div xmlns="http://www.w3.org/1999/xhtml"
         style="white-space: pre-wrap; font: 400 22px/1.45 ui-sans-serif, Inter; color: #CFD8E3;">
      ${safeBody}
    </div>
  </foreignObject>

  <!-- Footer pill -->
  <rect x="60" y="580" width="300" height="34" class="pill"/>
  <text x="80" y="603" class="mono">jyoti.app • Generated by JyotAI</text>
</svg>`;
}

export async function GET(req: NextRequest, { params }: { params: { id: string } }) {
  try {
    const id = params?.id;
    if (!id) return new NextResponse("Missing id", { status: 400 });

    const session = req.cookies.get("session")?.value;
    if (!session) return new NextResponse("Not authenticated", { status: 401 });

    const decoded = await adminAuth.verifySessionCookie(session, true);
    const uid = decoded.uid;

    const snap = await adminDb
      .collection("users").doc(uid)
      .collection("predictions").doc(id)
      .get();

    if (!snap.exists) return new NextResponse("Prediction not found", { status: 404 });

    const pred = snap.data() || {};
    const title = pred.query || "Your Question";
    const body =
      pred.prediction ||
      "No content available yet.";

    const svg = svgTemplate(title, body);

    return new NextResponse(svg, {
      status: 200,
      headers: {
        "Content-Type": "image/svg+xml; charset=utf-8",
        "Cache-Control": "no-store",
      },
    });
  } catch (err) {
    console.error("[share] error:", err);
    return new NextResponse("Failed to render", { status: 500 });
  }
}
